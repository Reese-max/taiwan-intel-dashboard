// 從機關地址（如「270宜蘭縣蘇澳鎮蘇新路101號」）抽出縣市名並回傳中心座標。
export function countyCoordFromAddr(addr) {
  if (!addr) return null;
  for (const name of Object.keys(COUNTY_CENTER)) {
    if (addr.includes(name)) {
      const [lat, lng] = COUNTY_CENTER[name];
      // 正規化縣市名（台→臺）供 region 顯示
      const region = name.replace(/^台/, "臺");
      return { lat, lng, region };
    }
  }
  return null;
}
